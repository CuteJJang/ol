import math  # 수학 관련 기능을 사용하기 위해 math 모듈 불러오기

반지름 = 3  # 원의 반지름
원주율 = math.pi  # 더 정확한 원주율 값 사용 (3.141592...)

원의둘레 = 반지름 * 2 * 원주율  # 원의 둘레 공식: 2πr
원의넓이 = 반지름 * 반지름 * 원주율  # 원의 넓이 공식: πr²

print("원의 둘레:", 원의둘레)  # 결과 출력
print("원의 넓이:", 원의넓이)